package day15_WhileLoop;

public class WhileLoopIntro {
    public static void main(String[] args) {

        if(true){
            System.out.println("Hello World");
        }

        System.out.println("----------------------------------------");
        for (int i = 0; i < 10; i++) {// bunda 10 a kadar devam eder

        }


        System.out.println("---------------------------------------------");
        while(true){
           System.out.println("Hello World");// bu while loop statement will be repedent until the conditions becomes false
        }



    }
}
