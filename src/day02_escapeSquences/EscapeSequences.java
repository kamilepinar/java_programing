package day02_escapeSquences;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println(" Hello Students\nToday we have Java class");
        System.out.println("------------------------------------------");
        System.out.println("\tJava programing language");
        System.out.println(" \t\tJava Developer course Batch 1");

        System.out.println("--------------------------------------------");

        System.out.println("\\");
        System.out.println("My Favorite Tv show is \"Game of Thrones\".");

        /* 1. \n New Line
        2.\t  give space
        3. \\ back slash
        4. \" double quote
         */




    }


}
